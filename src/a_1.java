public class a_1 {
    public static void main(String[] args) {


        //변수타입 변수 = 값
        int a;
        //Integer
        a = 1;  //정수형   원시타입
//        a = Integer.MAX_VALUE; 최대값
//        a = Integer.MIN_VALUE; 최소값
     double b;
     //Double
     b = 1.0; //실수형  원시타입
     char c;
     //Character
     c = 'c' ;  //문자형   원시타입 유니코드로 나타냄
     boolean d;
     //Boolean
     d = true; //논리타입 원시타입
     String e;
     e = "ee"; //문자열 레퍼런스타입




    }
}